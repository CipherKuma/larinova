export function MultiSpecialtyPreview() {
  const [activeIndex, setActiveIndex] = useState(0);

  useEffect(() => {
    const interval = setInterval(() => {
      setActiveIndex((prev) => (prev + 1) % specialties.length);
    }, 1200);
    return () => clearInterval(interval);
  }, []);

  return (
    <div className="h-full w-full p-3 flex flex-col items-center justify-center">
      {/* Specialty grid */}
      <div className="grid grid-cols-3 gap-2 mb-2">
        {specialties.map((specialty, index) => (
          <div
            key={specialty.name}
            className={`w-9 h-9 rounded-lg flex items-center justify-center transition-all duration-300 ${
              index === activeIndex
                ? `${specialty.color} text-white scale-110 shadow-lg`
                : "bg-secondary/50 text-muted-foreground"
            }`}
          >
            {specialty.icon}
          </div>
        ))}
      </div>

      {/* Active specialty name */}
      <p className="text-[10px] font-medium text-foreground">
        {specialties[activeIndex].name}
      </p>
      <p className="text-[9px] text-muted-foreground">
        +40 specialties supported
      </p>
    </div>
  );
}
